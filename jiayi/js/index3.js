var $buttonLeft = $(".button-left");
var $buttonRight = $(".button-right");
var $ul = $("#brand-data>ul");
// 计数器
var buttonCount = 0;
// 节流
var onOrOff = true;
$buttonLeft.click(function() {
	if (!onOrOff) return;
	onOrOff = false;
	buttonCount--;
	if (buttonCount <= 0) {
		$buttonLeft.css("display", "none");
		$ul.stop().animate({
			"left": -270 * buttonCount
		}, 800);
		// isAnimate();
		buttonCount = 0;
		// console.log(buttonCount);
	} else {
		$buttonLeft.css("display", "block");
		$buttonRight.css("display", "block");
		$ul.stop().animate({
			"left": -270 * buttonCount
		}, 800);
		// isAnimate();
		// console.log(buttonCount);
	}
	setTimeout(function() {
		onOrOff = true;
	}, 800);
});
$buttonRight.click(function() {
	if (!onOrOff) return;
	onOrOff = false;
	buttonCount++;
	if (buttonCount >= 5) {
		$buttonRight.css("display", "none");
		$ul.stop().animate({
			"left": + -270 * buttonCount
		}, 1000);
	} else {
		$buttonLeft.css("display", "block");
		$buttonRight.css("display", "block");
		$ul.stop().animate({
			"left": + -270 * buttonCount
		}, 800);
	}
	setTimeout(function() {
		onOrOff = true;
	}, 750);
});
